let vitesse = parseFloat(prompt("Quelle est la vitesse "));
let energiCinetique =  (1/2) * masse * Math.pow(vitesse , 2);

alert(`l'enegie cinetique total est: ${energiCinetique.toFixed(2)} joules`)

